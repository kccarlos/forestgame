package hk.edu.polyu.comp.comp2021.jungle.model;

/**
 * The enum Game state.
 */
public enum GameState
{
    /**
     * Player 1 move game state.
     */
    PLAYER1MOVE,
    /**
     * Player 2 move game state.
     */
    PLAYER2MOVE,
    /**
     * Player 1 win game state.
     */
    PLAYER1WIN,
    /**
     * Player 2 win game state.
     */
    PLAYER2WIN,
    /**
     * Prestart game state.
     */
    PRESTART
}

